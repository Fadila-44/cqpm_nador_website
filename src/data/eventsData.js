function parseDate(str) {
  if (!str) return null;
  const native = new Date(str);
  if (!Number.isNaN(native)) return native;
  const parts = str.trim().split(/[\s,]+/);
  if (parts.length >= 3) {
    const d = parseInt(parts[0], 10);
    const m = MONTH_MAP[parts[1].toLowerCase()];
    const y = parseInt(parts[2], 10);
    if (!Number.isNaN(d) && m !== undefined && !Number.isNaN(y)) return new Date(y, m, d);
  }
  return null;
}

function getSortTimestamp(event) {
  const raw = event.date?.en || event.date?.fr || event.date?.ar || "";
  const parsed = parseDate(raw);
  return parsed ? parsed.getTime() : Infinity;
}

export function buildEventsFromStatic() {
  const all = [];
  all.push({
    id: "featured",
    categoryKey: EVENT_DATA.featured.categoryKey,
    image: EVENT_DATA.featured.image,
    photos: EVENT_DATA.featured.photos || [],
    icon: null,
    date: { fr: EVENT_DATA.featured.fr.date, ar: EVENT_DATA.featured.ar.date, en: EVENT_DATA.featured.en.date },
    fr: { title: EVENT_DATA.featured.fr.title, text: EVENT_DATA.featured.fr.text },
    ar: { title: EVENT_DATA.featured.ar.title, text: EVENT_DATA.featured.ar.text },
    en: { title: EVENT_DATA.featured.en.title, text: EVENT_DATA.featured.en.text },
  });
  EVENT_DATA.side.forEach((item, i) => {
    all.push({
      id: `side-${i}`,
      categoryKey: item.categoryKey,
      image: item.image || null,
      photos: item.photos || [],
      icon: null,
      date: { fr: item.fr.date || "", ar: item.ar.date || "", en: item.en.date || "" },
      fr: { title: item.fr.title, text: item.fr.text },
      ar: { title: item.ar.title, text: item.ar.text },
      en: { title: item.en.title, text: item.en.text },
    });
  });
  EVENT_DATA.cards.forEach((card, i) => {
    all.push({
      id: `card-${i}`,
      categoryKey: card.categoryKey,
      image: card.image || null,
      photos: card.photos || [],
      icon: card.icon || null,
      date: card.date,
      fr: card.fr,
      ar: card.ar,
      en: card.en,
    });
  });

  const withDate = all.filter((e) => getSortTimestamp(e) !== Infinity);
  const withoutDate = all.filter((e) => getSortTimestamp(e) === Infinity);
  withDate.sort((a, b) => getSortTimestamp(b) - getSortTimestamp(a));
  return [...withDate, ...withoutDate];
}

export function mergeCmsEvents(staticEvents, cmsEvents = []) {
  if (!cmsEvents.length) return staticEvents;

  const cmsMapped = cmsEvents.map((event) => ({
    id: event.slug || `cms-${event.id}`,
    categoryKey: event.category,
    image: event.image || null,
    photos: event.photos || [],
    icon: event.icon || null,
    date: event.date || {
      fr: event.fr?.date || event.content?.fr?.date || "",
      en: event.en?.date || event.content?.en?.date || "",
      ar: event.ar?.date || event.content?.ar?.date || "",
    },
    fr: {
      title: event.fr?.title || event.content?.fr?.title || "",
      text: event.fr?.text || event.content?.fr?.text || "",
    },
    ar: {
      title: event.ar?.title || event.content?.ar?.title || "",
      text: event.ar?.text || event.content?.ar?.text || "",
    },
    en: {
      title: event.en?.title || event.content?.en?.title || "",
      text: event.en?.text || event.content?.en?.text || "",
    },
  }));

  const cmsIds = new Set(cmsMapped.map((e) => e.id));
  return [...cmsMapped, ...staticEvents.filter((e) => !cmsIds.has(e.id))];
}

export function getAllEvents(cmsEvents = []) {
  return mergeCmsEvents(buildEventsFromStatic(), cmsEvents);
}

export function eventsToNewsItems(events, lang = "fr") {
  const categoryWord = { fr: "Événement", ar: "حدث", en: "Event" };
  return events.map((event) => {
    const loc = event[lang] || event.fr;
    const date = event.date?.[lang] || event.date?.fr || "";
    const cat = (CATEGORY_LABELS[event.categoryKey] || {})[lang] || categoryWord[lang] || categoryWord.fr;
    return {
      eventId: event.id,
      image: event.image || "https://i.pinimg.com/1200x/b4/d9/92/b4d9927d56f422bc280a02a013f69c8c.jpg",
      category: cat,
      date,
      title: loc.title,
      text: loc.text,
      alt: loc.title,
    };
  });
}
